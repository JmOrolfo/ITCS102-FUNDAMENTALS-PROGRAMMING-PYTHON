#CHRISTMAS TREE
for i in range(2, 6, 1):
    for iu in range(0, 7):
        print(" ", end=" ")
    for io in range(6, i, -1):
        print(" ", end=" ")
    for ia in range(3, i):
        print("*", end=" ")
    for ie in range(2, i, 1):
        print("*", end=" ")
    print()
  
for e in range(1, 3, 1):
    for ei in range(0, 7):
        print(" ", end=" ")
    for eu in range(0 - 1, e, 1):
        print(" ", end=" ")
    for eo in range(2, e, -1):
        print("*", end=" ")
    for ea in range(3, e, -1):
        print("*", end=" ")
    print()
    
for u in range(2, 9, 1):
    for ui in range(12, u, -1):
        print(" ", end=" ")
    for uo in range(2 , u, 1):
        print("*", end=" ")
    for ua in range(1, u):
        print("*", end=" ")
    print()
    
for o in range(2, 10, 1):
    for oi in range(12, o, -1):
        print(" ", end=" ")
    for ou in range(1, o):
        print("*", end=" ")
    for oa in range(2, o):
        print("*", end=" ")
    print()
    
for a in range(2, 13, 1):
    for ai in range(12, a, -1):
        print(" ", end=" ")
    for au in range(1, a):
        print("*", end=" ")
    for ao in range(2, a):
        print("*", end=" ")
    print()
    
